#include <stdbool.h>
#include "ft_string.h"

int	is_meta_char(int c)
{
	return (c == '|' || c == '&' || c == ';' || c == '<' || c == '>'
		|| c == '(' || c == ')' || c == '{' || c == '}' || c == '\n'
		|| c == ' ' || c == '\t' || c == '$' || c == '`' || c == '\\'
		|| c == '"' || c == '\'' || c == '#');
}

bool	is_metachar(int c)
{
	const char	*metachars = "|&;<>(){}[]'\"`\\$#~*?";

	return (c && ft_strchr(metachars, c) != NULL);
}
