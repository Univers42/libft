int	is_meta_char(int c)
{
	return (c == '|' || c == '&' || c == ';' || c == '<' || c == '>'
			|| c == '(' || c == ')' || c == '{' || c == '}' || c == '\n'
			|| c == ' ' || c == '\t' || c == '$' || c == '`' || c == '\\'
			|| c == '"' || c == '\'' || c == '#');
}